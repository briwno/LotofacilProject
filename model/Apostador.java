package LotofacilProject.model;

public class Apostador {
    private String nome;
    private String email;
    private String senha;
    private String cpf;
    private String telefone;
    
    public Apostador(String nome, String email, String senha, String cpf, String telefone) {
        this.nome = nome;
        this.email = email;
        this.senha = senha;
        this.cpf = cpf;
        this.telefone = telefone;
    }
    
}
